<?php
class Solution {
    /**
     * @param Integer[] $commands
     * @param Integer[][] $obstacles
     * @return Integer
     */
    function robotSim($commands, $obstacles) {
        $dx = [0,1,0,-1]; // x轴
        $dy = [1,0,-1,0]; // y轴
        $x = 0; $y = 0; $di = 0; // x轴 y轴 方向
        $obstacleSet = [];
        foreach ($obstacles as $obstacle) { // 坐标转map
            // $xy = $obstacle[0].'-'.$obstacle[1].'-';
            // $obstacleSet[$xy] = true;
            $ox = $obstacle[0] + 30000;
            $oy = $obstacle[1] + 30000;
            $obstacleSet[(($ox << 16) + $oy)] = true;
        }
        $ans = 0;
        foreach ($commands as $cmd) {
            if ($cmd == -2) { // 向左 因为涉及循环所以需要取模
                $di = ($di + 3) % 4;
            } elseif($cmd == -1) { // 向右
                $di = ($di + 1) % 4;
            } else {
                for ($k=0; $k < $cmd; ++$k) {
                    $nx = $x + $dx[$di];
                    $ny = $y + $dy[$di];
                    // $code = $nx.'-'.$ny.'-';
                    $code = (($nx + 30000) << 16) + ($ny + 30000);
                    if (!isset($obstacleSet[$code])) { // 如果当前点不在障碍物里面，那么久更新计算
                        $x = $nx;
                        $y = $ny;
                        $ans = max($ans, $x*$x + $y*$y);
                    }
                }
            }
        }
        return $ans;
    }
}

